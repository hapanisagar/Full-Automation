package L.Array;

public class SingleDimArray2 {
    public static void main(String[] args) {
        String name[]=new String[3];
        name[0]="Sagar";
        name[1]="Darshan";
        name[2]="Tester3";
        System.out.println(name[1]);
        for(int i=0;i<name.length;i++){
            System.out.println(name[i]);
        }
    }
}
