package H.Condition;

public class Switchcase {
    public static void main(String[] args) {
        int size=46;
        switch(size){
            case 28: System.out.println("Size="+'S');break;
            case 32: System.out.println("Size="+'M');break;
            case 34: System.out.println("Size="+'L');break;
            case 37: System.out.println("Size="+"XL");break;
            case 40: System.out.println("Size="+"XXL");break;
            case 42: System.out.println("Size="+"XXXL");break;
            case 46: System.out.println("Size="+"4XL");break;
        }
    }
}

